"""
Desafio 016: Quebrando um Numero

Crie um programa que leia um numero real qualquer pelo teclado e mostre na tela a sua porcao inteira.

Ex:
Digite um numero: 6.127
O numero 6.127 tem a parte inteira 6.
"""

n = float(input('Digite um Numero Real (ex: 6.127): '))
nr = int(n)
print('\nO numero inteiro de {} e {}!'.format(n, nr))
